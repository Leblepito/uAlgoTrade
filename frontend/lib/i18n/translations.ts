export function t(locale: Locale, key: TranslationKey, vars?: Record<string, string | number>): string {
  const localeData = translations[locale] ?? translations.en;
  const val = (localeData as Record<string, string>)[key] ?? (translations.en as Record<string, string>)[key] ?? key;
  if (!vars) return val;
  return Object.entries(vars).reduce((str, [k, v]) => str.replace(`{${k}}`, String(v)), val);
}
